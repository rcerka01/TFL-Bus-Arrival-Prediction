package models

import models.autogenerated.Tables._
import slick.driver.MySQLDriver.api._

/**
 * Created by raitis on 10/09/15.
 */
object JobDAO {

  val db = Database.forConfig("db_tfl")
  val jobs: TableQuery[Jobs] = TableQuery[Jobs]


  def findJobsByUserId(id: Int) = {
    db.run(Jobs.filter(_.usersId === id).result)
  }


  def insert(jobsRow: JobsRow) = {
    db.run(jobs += jobsRow)
  }


  def deleteJob(id: Int) = {
    db.run(Jobs.filter(_.id === id).delete)
  }

}

